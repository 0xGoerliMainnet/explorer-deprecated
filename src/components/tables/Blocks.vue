<template>
  <table class="w-full">
    <thead>
      <tr class="opacity-25 text-xs">
        <th @click="sortBy('id')" class="p-4 pl-10 text-left">ID</th>
        <th @click="sortBy('height')" class="p-4 text-left hidden md:table-cell">Height</th>
        <th @click="sortBy('timestamp')" class="p-4 text-left">Timestamp</th>
        <th @click="sortBy('numberOfTransactions')" class="p-4 text-right hidden md:table-cell">Transactions</th>
        <th @click="sortBy('generatorPublicKey')" class="p-4 text-right hidden lg:table-cell">Generated by</th>
        <th @click="sortBy('totalForged')" class="p-4 text-right">Reward (ARK)</th>
        <th @click="sortBy('totalFee')" class="p-4 pr-10 text-right">Fee (ARK)</th>
      </tr>
    </thead>
    <tbody>
      <tr v-for="block in sortedBlocks" :key="block.id">
        <td class="p-4 pl-10 text-left border-none">
          <block-link :id="block.id"></block-link>
        </td>

        <td class="p-4 text-left border-none hidden md:table-cell">
          {{ block.height }}
        </td>

        <td class="p-4 text-left border-none">
          {{ readableTimestamp(block.timestamp) }}
        </td>

        <td class="p-4 text-right hidden md:table-cell border-none">
          {{ block.numberOfTransactions }}
        </td>

        <td class="p-4 text-right border-none hidden lg:table-cell">
          <wallet-link :public-key="block.generatorPublicKey"></wallet-link>
        </td>

        <td class="p-4 text-right border-none">
          {{ readableCrypto(block.totalForged) }}
        </td>

        <td class="p-4 pr-10 text-right border-none">
          {{ readableCrypto(block.totalFee) }}
        </td>
      </tr>
    </tbody>
  </table>
</template>

<script type="text/ecmascript-6">
import Currency from '@/components/utils/Currency'
import WalletLink from '@/components/links/Wallet'
import BlockLink from '@/components/links/Block'
import SortableTable from '@/mixins/sortable-table'

export default {
  mixins: [SortableTable],

  components: {
    Currency,
    BlockLink,
    WalletLink,
  },

  props: {
    blocks: {
      type: Array,
      required: true,
    },
  },

  computed: {
    sortedBlocks() {
      return _.orderBy(this.blocks, this.sortKey, this.sortDirection)
    }
  }
}
</script>

